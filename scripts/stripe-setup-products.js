#!/usr/bin/env node
// scripts/stripe-setup-products.js
// One-time setup: creates all NYCWine ad products + recurring
// prices in Stripe via REST (no npm package needed).
//
// Usage:  node scripts/stripe-setup-products.js
//
// Matches the 7 ad slots in pages/advertise-buy.js and the
// PRICE_IDS map in pages/api/checkout.js.

const https  = require('https');
const qs     = require('querystring');

const SECRET_KEY = process.env.STRIPE_SECRET_KEY;
if (!SECRET_KEY) { console.error('Set STRIPE_SECRET_KEY in your environment first.'); process.exit(1); }

function stripePost(path, params) {
  return new Promise((resolve, reject) => {
    const body = qs.stringify(params);
    const req  = https.request({
      hostname: 'api.stripe.com',
      path,
      method:   'POST',
      headers:  {
        'Authorization':  `Bearer ${SECRET_KEY}`,
        'Content-Type':   'application/x-www-form-urlencoded',
        'Content-Length': Buffer.byteLength(body),
      },
    }, (res) => {
      let data = '';
      res.on('data', (chunk) => { data += chunk; });
      res.on('end', () => {
        const parsed = JSON.parse(data);
        if (parsed.error) reject(new Error(`${parsed.error.type}: ${parsed.error.message}`));
        else resolve(parsed);
      });
    });
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

// Matches the AD_SLOTS in pages/advertise-buy.js
const AD_SLOTS = [
  { id: 'header',  name: 'NYCWine - Header Banner Ad',         description: 'Sitewide header placement (468x60px) -- visible on every page.',                monthlyPrice: 300 },
  { id: 'event',   name: 'NYCWine - Featured Event',           description: 'Featured card on Events directory page + homepage feed.',                       monthlyPrice: 25 },
  { id: 'bar',     name: 'NYCWine - Featured Wine Bar',        description: 'Featured sidebar card on the Wine Bars directory page.',                       monthlyPrice: 25 },
  { id: 'store',   name: 'NYCWine - Featured Wine Store',      description: 'Featured sidebar card on the Wine Stores directory page.',                     monthlyPrice: 25 },
  { id: 'winery',  name: 'NYCWine - Featured Winery',          description: 'Featured sidebar card on the Wineries directory page.',                        monthlyPrice: 25 },
  { id: 'sidebar', name: 'NYCWine - Events Sidebar Ad',        description: 'Banner (300x90px) in the homepage Events sidebar.',                            monthlyPrice: 100 },
  { id: 'social',  name: 'NYCWine - Social Page Ad',           description: 'Banner (300x250px) in the right sidebar of the Wine Social page.',              monthlyPrice: 75 },
];

const TIERS = [
  { key: 'monthly',   label: '1 Month',         months: 1,  discount: 0,    interval: 'month', intervalCount: 1 },
  { key: 'quarterly', label: '3 Months (-10%)', months: 3,  discount: 0.10, interval: 'month', intervalCount: 3 },
  { key: 'annual',    label: '12 Months (-30%)',months: 12, discount: 0.30, interval: 'year',  intervalCount: 1 },
];

function cents(monthlyDollars, months, discount) {
  return Math.round(monthlyDollars * months * (1 - discount) * 100);
}

async function main() {
  console.log('======================================================');
  console.log('  NYCWine -- Stripe Product & Price Setup');
  console.log(`  Mode: ${SECRET_KEY.startsWith('sk_live') ? 'LIVE' : 'TEST'}`);
  console.log('======================================================\n');

  const envLines = ['\n# Stripe Price IDs (generated by stripe-setup-products.js)'];

  for (const slot of AD_SLOTS) {
    console.log('> Creating product: ' + slot.name);
    const product = await stripePost('/v1/products', {
      name:                slot.name,
      description:         slot.description,
      'metadata[slot_id]': slot.id,
    });
    console.log('  Product ID: ' + product.id);

    for (const tier of TIERS) {
      const unitAmount = cents(slot.monthlyPrice, tier.months, tier.discount);
      const dollars    = '$' + (unitAmount / 100).toLocaleString('en-US', { minimumFractionDigits: 2 });
      const perMo      = '$' + (unitAmount / 100 / tier.months).toFixed(2) + '/mo';

      const priceParams = {
        product:               product.id,
        unit_amount:           unitAmount,
        currency:              'usd',
        'recurring[interval]': tier.interval,
        nickname:              slot.id + ' -- ' + tier.label,
        'metadata[slot_id]':   slot.id,
        'metadata[tier]':      tier.key,
        'metadata[months]':    tier.months,
        'metadata[discount]':  tier.discount,
      };
      if (tier.intervalCount > 1) {
        priceParams['recurring[interval_count]'] = tier.intervalCount;
      }

      const price = await stripePost('/v1/prices', priceParams);
      console.log('  [OK] ' + tier.label.padEnd(20) + ' ' + dollars.padStart(10) + '  (' + perMo.padStart(10) + ')  ' + price.id);

      const envKey = 'STRIPE_PRICE_' + slot.id.toUpperCase() + '_' + tier.key.toUpperCase();
      envLines.push(envKey + '=' + price.id);
    }
    console.log();
  }

  console.log('======================================================');
  console.log('  Add these to Vercel env vars:');
  console.log('======================================================');
  console.log(envLines.join('\n'));
  console.log('\n[OK] All done.');
}

main().catch(err => {
  console.error('\n[ERROR] ' + err.message);
  process.exit(1);
});
